package exemplos;

/**
 * do/while
 */
public class Exemplo01 {

    public static void main(String[] args) {
        // 1. inicio
        // 2. condição
        // 3. passo (incremento)

        int cont = 10; // 1

        while (cont < 5) { // 2
            System.out.println(": " + cont);
            cont++; // 3
        }

    }

}